var numbers = [78, 5, 20, 10, 8, 9, 45, 100];
console.log(numbers);

// use push method to add element in arry
// push element in arry
numbers.push(200);
console.log(numbers);

// use pop method to remove element from arry
// pop element from arry
numbers.pop();
console.log(numbers);

// use shift method to remove element from arry
// shift element from arry
numbers.shift();
console.log(numbers);

// use unshift method to add element in arry
// unshift element in arry
numbers.unshift(1);
console.log(numbers);

// use splice method to add element in arry
// slice element from arry
var sliceElement = numbers.slice(2, 5);
console.log(sliceElement);
console.log(numbers);