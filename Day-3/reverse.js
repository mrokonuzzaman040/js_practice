//Forword for Loop
var number = 1;

while(number <= 10){
    console.log(number);
    number++;
}

//reverse for loop
var num = 10;

while( num >= 1){
    console.log(num);
    num--;
}

// foreword for loop
for (var i = 1; i <= 10; i++){
    console.log(i);
}

// reverse for loop
for (var i = 10; i >= 1; i--){
    console.log(i);
}
